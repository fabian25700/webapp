// Hilfsfunktion: ISO-Code → Flaggen-Emoji
function getFlagEmoji(isoCode) {
  if (!isoCode || isoCode.length !== 2) return "🏳️";
  return isoCode.toUpperCase().replace(/./g, char =>
    String.fromCodePoint(127397 + char.charCodeAt(0))
  );
}

